/**
 * Get tutorials by category
 */
export function getTutorialsByCategory(category: string): Tutorial[] {
  return TUTORIALS.filter((t) => t.category === category).sort((a, b) => a.order - b.order);
}

/**
 * Get tutorial by ID
 */
export function getTutorialById(id: string): Tutorial | undefined {
  return TUTORIALS.find((t) => t.id === id);
}

/**
 * Get all tutorials for a specific role
 */
export function getTutorialsForRole(role: string): Tutorial[] {
  return TUTORIALS.filter((t) => t.roles.includes(role as any));
}
